function t2s(s) {
  return s.split('').map(c => T2S_CHARS[c] || c).join('');
}

async function fetchOne(name) {
  const url = 'https://zh.wikisource.org/wiki/%E5%91%A8%E6%98%93/' + encodeURIComponent(name);
  const res = await fetch(url);
  const html = await res.text();
  // extract clean text
  const cleaned = html
    .replace(/<script[\s\S]*?<\/script>/g, '')
    .replace(/<style[\s\S]*?<\/style>/g, '')
    .replace(/<[^>]+>/g, '\n')
    .replace(/&[a-z#0-9]+;/g, '');
  const lines = cleaned.split('\n').map(l => l.trim()).filter(Boolean);

  // Find "易經：" anchor; the next non-empty CJK line is 卦名, then 卦辞 starts with "："
  let guaci = null, daxiang = null, trigramsLine = null;
  for (let i = 0; i < lines.length - 5; i++) {
    if (lines[i] === '易經：') {
      // i+1 = name, i+2 = "：xxx" (卦辞)
      const ci = lines[i + 2];
      if (ci && ci.startsWith('：')) {
        guaci = ci.slice(1).trim().replace(/。$/, '');
      } else {
        // sometimes name+卦辞 inline
        guaci = (lines[i + 1] + ci || '').replace(/^[^：]*：/, '').trim();
      }
      break;
    }
  }
  // 大象 — line that immediately follows "象曰：" and is not 爻辞 echo (爻辞 echo lines start with 「 or quotes)
  for (let i = 0; i < lines.length - 1; i++) {
    if (lines[i] === '象曰：') {
      const next = lines[i + 1];
      if (next && !next.startsWith('「') && next.length < 50) {
        daxiang = next;
        break;
      }
    }
  }
  // trigrams line "乾下乾上" pattern
  for (const l of lines) {
    if (/^[乾兌離震巽坎艮坤][下上][乾兌離震巽坎艮坤][下上]$/.test(l)) {
      trigramsLine = l;
      break;
    }
  }

  return { name, guaci, daxiang, trigramsLine };
}

async function main() {
  const out = [];
  for (let i = 0; i < GUA64.length; i++) {
    const [name, upper, lower] = GUA64[i];
    process.stderr.write('[' + (i + 1) + '/64] ' + name + '… ');
    let raw;
    for (let attempt = 0; attempt < 3; attempt++) {
      try { raw = await fetchOne(name); break; }
      catch (e) { process.stderr.write('retry ' + e.message + ' '); await new Promise(r => setTimeout(r, 800)); }
    }
    if (!raw || !raw.guaci) {
      process.stderr.write('MISS_GUACI\n');
      out.push({ id: i + 1, name: T2S[name] || name, symbol: SYMBOLS[i], upper, lower,
                 guaci: null, daxiang: null, _raw_name: name });
      continue;
    }
    out.push({
      id: i + 1,
      name: T2S[name] || name,
      symbol: SYMBOLS[i],
      upper: TRIGRAMS[upper - 1],
      lower: TRIGRAMS[lower - 1],
      guaci: t2s(raw.guaci),
      daxiang: t2s(raw.daxiang || ''),
    });
    process.stderr.write('ok ' + raw.guaci.slice(0, 30) + '\n');
    await new Promise(r => setTimeout(r, 200));
  }
  fs.writeFileSync(OUT, JSON.stringify(out, null, 2));
  console.error('\nwrote ' + OUT + ' (' + out.length + ' gua, ' + out.filter(x => x.guaci).length + ' with 卦辞)');
}

main().catch(e => { console.error(e); process.exit(1); });
